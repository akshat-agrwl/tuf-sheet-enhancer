// Regenerates ../companies.js (TUF_COMPANIES) from:
//   - tools/lc-index.json  — LeetCode company tags (run build-index.js first)
//   - tools/tuf-lc-map.js  — TUF problem -> LeetCode title(s) mapping
//   - tools/gfg-tags.json  — GeeksforGeeks company tags for problems not on LeetCode
// Run: node tools/gen-companies.js
const fs = require("fs");
const path = require("path");

const CAP = 12;
const idx = require(path.join(__dirname, "lc-index.json"));
const map = require(path.join(__dirname, "tuf-lc-map.js"));
const gfg = require(path.join(__dirname, "gfg-tags.json"));

const dataCode = fs.readFileSync(path.join(__dirname, "..", "data.js"), "utf8");
const { TUF_DATA } = new Function(dataCode + "; return { TUF_DATA };")();
const tufKeys = Object.keys(TUF_DATA);

// The dataset's Frequency is normalized per company (a company's hottest problem
// is 100 regardless of company size), so raw-freq ranking surfaces tiny companies.
// Weight by company size: score = freq * log10(1 + #problems tagged to company).
const companySize = {};
for (const k of Object.keys(idx)) {
  for (const [c] of idx[k].companies) companySize[c] = (companySize[c] || 0) + 1;
}

// Display casing for the dataset's lowercase folder names.
const NAME_FIX = {
  athenahealth: "Athenahealth", blinkit: "Blinkit", carwale: "CarWale",
  ciena: "Ciena", fourkites: "FourKites", instabase: "Instabase", jio: "Jio",
  "josh technology": "Josh Technology", opentext: "OpenText", oyo: "OYO",
  peak6: "PEAK6", "persistent systems": "Persistent Systems", razorpay: "Razorpay",
  redbus: "redBus", smartnews: "SmartNews", tcs: "TCS", thoughtspot: "ThoughtSpot",
  "zeta suite": "Zeta Suite",
};

// two tuf keys share the undirected-cycle GFG page
const gfgAlias = {
  detectacycleinundirectedgraphusingbfs: "detectacycleinundirectedgraph",
  detectacycleinundirectedgraphusingdfs: "detectacycleinundirectedgraph",
};

const unknownTargets = [];
const result = {};
const stats = { lc: 0, gfg: 0, both: 0, none: 0 };

for (const key of tufKeys) {
  const targets = map[key] !== undefined ? map[key] : [key];
  // merge LC companies across targets, keeping max frequency per company
  const merged = new Map();
  for (const t of targets) {
    const entry = idx[t];
    if (!entry) { unknownTargets.push(`${key} -> ${t}`); continue; }
    for (const [name, freq] of entry.companies) {
      merged.set(name, Math.max(merged.get(name) || 0, freq));
    }
  }
  const score = ([c, f]) => f * Math.log10(1 + (companySize[c] || 0));
  let companies = [...merged.entries()]
    .sort((a, b) => score(b) - score(a))
    .map(([n]) => NAME_FIX[n] || n);

  const gfgList = gfg[gfgAlias[key] || key] || [];
  const hasLc = companies.length > 0;
  const hasGfg = gfgList.length > 0;
  // GFG tags fill in after LC's freq-ranked list, deduped
  for (const name of gfgList) if (!companies.includes(name)) companies.push(name);

  companies = companies.slice(0, CAP);
  if (hasLc && hasGfg) stats.both++;
  else if (hasLc) stats.lc++;
  else if (hasGfg) stats.gfg++;
  else stats.none++;
  if (companies.length) result[key] = companies;
}

if (unknownTargets.length) {
  console.error("UNKNOWN LC TARGETS:\n" + unknownTargets.join("\n"));
  process.exit(1);
}

const covered = Object.keys(result).length;
console.log(`covered: ${covered}/${tufKeys.length}`, stats);
console.log("uncovered:", tufKeys.filter((k) => !result[k]).join(", ") || "(none)");

const lines = tufKeys
  .filter((k) => result[k])
  .map((k) => `  ${/^[a-z_$][a-z0-9_$]*$/i.test(k) ? k : JSON.stringify(k)}: ${JSON.stringify(result[k])},`);

const out = `// Company tags per problem — which companies have asked it in interviews.
// Keyed like TUF_DATA (normalized problem title). Ranked by how frequently the
// company asks it (LeetCode company tags), followed by GeeksforGeeks tags for
// problems that aren't on LeetCode. Capped at ${CAP} companies per problem.
// Generated by tools/gen-companies.js — see README "Company tags".
const TUF_COMPANIES = {
${lines.join("\n")}
};
`;
fs.writeFileSync(path.join(__dirname, "..", "companies.js"), out);
console.log("wrote companies.js,", out.length, "bytes");
